/**
 * struct list_s - new struct type defining a linked list
 * @dir: A directory path
 * @next: A pointer to another struct list_s
 */
typedef struct list_s
{
	char *dir;
	struct list_s *next;
} list_t;

/**
 * struct builtin_s - new struct type defining builtin commands
 * @name: The name of the builtin command
 * @f: A function pointer to the builtin command's function
 */
typedef struct builtin_s
{
	char *name;
	int (*f)(char **argv, char **front);
} builtin_t;

/**
 * struct alias_s - A new struct defining aliases
 * @name: The name of the alias
 * @value: The value of the alias
 * @next: A pointer to another struct alias_s
 */
typedef struct alias_s
{
	char *name;
	char *value;
	struct alias_s *next;
} alias_t;

/**
 * struct path_list_s - singly linked list of directories in PATH
 * @dir: directory name
 * @next: pointer to next node in list
 */
typedef struct path_list_s
{
    char *dir;
    struct path_list_s *next;
} path_list_t;


